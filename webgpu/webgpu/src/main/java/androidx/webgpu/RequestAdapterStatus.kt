         */
        public const val CallbackCancelled: Int = 0x00000002

        /** No suitable adapter was found. */
        public const val Unavailable: Int = 0x00000003

        /** An error occurred during the adapter request. */
        public const val Error: Int = 0x00000004
        internal val names: Map<Int, String> =
            mapOf(
